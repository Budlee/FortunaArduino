#ifndef PrintMod_h
#define PrintMod_h

#include <inttypes.h>
#include <stdio.h> // for size_t
#include <string.h>

class Print
{
  private:
    int write_error;
  public:
    Print() : write_error(0) {}
    virtual size_t write(uint8_t) = 0;
    size_t write(const char *str) {
      if (str == NULL) return 0;
      return write((const uint8_t *)str, strlen(str));
    }
    virtual size_t write(const uint8_t *buffer, size_t size);
};
#endif
